public class MappingChocolates {
    private int qty;
    private String Chocolate;

    public MappingChocolates(){}

    public MappingChocolates(int qyt, String chocolate) {
        this.qty = qty;
        Chocolate = chocolate;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public void setChocolate(String chocolate) {
        System.out.println(chocolate);
        Chocolate = chocolate;
    }

    public int getQty() {
        return qty;
    }

    public String getChocolate() {
        return Chocolate;
    }
}
